-- และระบบเก็บออเดอร์สำหรับการสั่งประกอบคอมพิวเตอร์

CREATE DATABASE IF NOT EXISTS smart_pc_builder DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE smart_pc_builder;

-- Drop existing tables to ensure a clean setup if re-imported
DROP TABLE IF EXISTS `order_items`;
DROP TABLE IF EXISTS `orders`;
DROP TABLE IF EXISTS `spec_case`;
DROP TABLE IF EXISTS `spec_psu`;
DROP TABLE IF EXISTS `spec_storage`;
DROP TABLE IF EXISTS `spec_gpu`;
DROP TABLE IF EXISTS `spec_ram`;
DROP TABLE IF EXISTS `spec_motherboard`;
DROP TABLE IF EXISTS `spec_cpu`;
DROP TABLE IF EXISTS `products`;
DROP TABLE IF EXISTS `categories`;

-- 1. ตารางหมวดหมู่อุปกรณ์ (Hardware Category)
CREATE TABLE `categories` (
  `id` INT AUTO_INCREMENT PRIMARY KEY,
  `slug` VARCHAR(50) NOT NULL UNIQUE, -- เช่น 'cpu', 'motherboard', 'ram'
  `name_th` VARCHAR(100) NOT NULL,    -- เช่น 'ซีพียู', 'เมนบอร์ด'
  `description` TEXT,
  `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 2. ตารางรวมสินค้าทั้งหมด (Products Master)
CREATE TABLE `products` (
  `id` INT AUTO_INCREMENT PRIMARY KEY,
  `category_id` INT NOT NULL,
  `brand` VARCHAR(100) NOT NULL,
  `model` VARCHAR(255) NOT NULL,
  `price` DECIMAL(10, 2) NOT NULL,
  `image_url` VARCHAR(255),
  `stock_quantity` INT DEFAULT 10,
  `specifications` JSON DEFAULT NULL,
  `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (`category_id`) REFERENCES `categories`(`id`) ON DELETE CASCADE
);

-- 3. ตารางสเปค CPU
CREATE TABLE `spec_cpu` (
  `product_id` INT PRIMARY KEY,
  `socket` VARCHAR(50) NOT NULL,      -- เช่น LGA1700, AM5
  `tdp_watt` INT NOT NULL,            -- สำหรับคำนวณกำลังไฟ
  FOREIGN KEY (`product_id`) REFERENCES `products`(`id`) ON DELETE CASCADE
);

-- 4. ตารางสเปค Motherboard
CREATE TABLE `spec_motherboard` (
  `product_id` INT PRIMARY KEY,
  `socket` VARCHAR(50) NOT NULL,      -- ตรงกับ CPU
  `ram_type` VARCHAR(20) NOT NULL,    -- DDR4, DDR5
  FOREIGN KEY (`product_id`) REFERENCES `products`(`id`) ON DELETE CASCADE
);

-- 5. ตารางสเปค RAM
CREATE TABLE `spec_ram` (
  `product_id` INT PRIMARY KEY,
  `ram_type` VARCHAR(20) NOT NULL,    -- DDR4, DDR5
  `capacity_gb` INT NOT NULL,
  FOREIGN KEY (`product_id`) REFERENCES `products`(`id`) ON DELETE CASCADE
);

-- 6. ตารางสเปค GPU (การ์ดจอ)
CREATE TABLE `spec_gpu` (
  `product_id` INT PRIMARY KEY,
  `tdp_watt` INT NOT NULL,            -- กำลังไฟที่ใช้งาน
  FOREIGN KEY (`product_id`) REFERENCES `products`(`id`) ON DELETE CASCADE
);

-- 7. ตารางสเปค Storage (SSD/HDD)
CREATE TABLE `spec_storage` (
  `product_id` INT PRIMARY KEY,
  FOREIGN KEY (`product_id`) REFERENCES `products`(`id`) ON DELETE CASCADE
);

-- 8. ตารางสเปค PSU (เพาเวอร์ซัพพลาย)
CREATE TABLE `spec_psu` (
  `product_id` INT PRIMARY KEY,
  `wattage` INT NOT NULL,             -- กำลังไฟสูงสุดที่จ่ายได้
  FOREIGN KEY (`product_id`) REFERENCES `products`(`id`) ON DELETE CASCADE
);

-- 9. ตารางสเปค Case (เคสคอมพิวเตอร์)
CREATE TABLE `spec_case` (
  `product_id` INT PRIMARY KEY,
  `form_factor_support` VARCHAR(255) NOT NULL, -- 'ATX, mATX, Mini-ITX'
  FOREIGN KEY (`product_id`) REFERENCES `products`(`id`) ON DELETE CASCADE
);

-- 10. ตารางใบสั่งประกอบคอมพิวเตอร์ (Orders)
CREATE TABLE `orders` (
  `id` VARCHAR(50) PRIMARY KEY,       -- รหัสคำสั่งซื้อ เช่น ORD-1001
  `customer_name` VARCHAR(100) NOT NULL,
  `customer_address` TEXT,
  `customer_phone` VARCHAR(20),
  `assembly_type` VARCHAR(50) DEFAULT 'none',
  `total_price` DECIMAL(10, 2) NOT NULL,
  `status` VARCHAR(50) DEFAULT 'assembling', -- assembling, shipped, completed
  `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 11. ตารางรายละเอียดรายการในใบสั่งประกอบ (Order Items)
CREATE TABLE `order_items` (
  `id` INT AUTO_INCREMENT PRIMARY KEY,
  `order_id` VARCHAR(50) NOT NULL,
  `product_id` INT NOT NULL,
  `category_slug` VARCHAR(50) NOT NULL,
  `price` DECIMAL(10, 2) NOT NULL,
  FOREIGN KEY (`order_id`) REFERENCES `orders`(`id`) ON DELETE CASCADE,
  FOREIGN KEY (`product_id`) REFERENCES `products`(`id`) ON DELETE CASCADE
);

